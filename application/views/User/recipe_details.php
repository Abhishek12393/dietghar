<div class="fn_desc">
<strong>Cauliflower Mash:</strong> Heat the olive oil in a large soup 
pot. Add the cauliflower and garlic. Saute for a minute or two, until 
the garlic is fragrant. Add the milk and 2 cups broth. Simmer for 10 
minutes or until soft. Add the white beans and mash roughly with the 
back of a large wooden spoon. It will be soupy - that is okay. Stir in 
the cornmeal and things will start to thicken a bit. Adjust the 
consistency by adding in the last cup of broth as needed. Stir in the 
cheese and season to taste.
